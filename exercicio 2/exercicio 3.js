for(let i = 1; i <= 10; i++){
    console.log(i);
}

/* 
Explicação do código:
Inicialização (let i = 1): A variável i começa com o valor 1.
Condição (i <= 10): O loop continua enquanto i for menor ou igual a 10.
Incremento (i++): Após cada iteração, i é aumentado em 1.
console.log(i): Imprime o valor atual de i no console em cada iteração.
 */